//SWITCH statments

let color="blue";

switch(color) {
    case "red" :
        console.log("STOP!")
        break;
    case "yellow" :
        console.log("SLOW DOWN!")
        break;
    case "green" :
        console.log("GO")
        break;
    default :
        console.log("Traffic light is broken")
}

